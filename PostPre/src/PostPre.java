
public class PostPre {

	public static void main(String[] args) {
		
		int i=5;
		System.out.println(i);
		i ++;
		int j=i;
		System.out.println(j);
		int k =8;
		k--;
		int l= k;
		System.out.println(l);
		int a =4;
		++a;
		
		System.out.println(a);
		int c =6;
		--c;
		System.out.println(c);
		

	}

}
